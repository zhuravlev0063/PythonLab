def polyglots():
    # Чтение количества школьников
    n = int(input("Введите количество школьников: "))

    # Переменные для хранения множеств
    all_languages = set()  # Множество для всех языков
    common_languages = None  # Множество для языков, известных всем школьникам

    for i in range(n):
        # Чтение количества языков, которые знает текущий школьник
        m = int(input(f"\nВведите количество языков для школьника {i + 1}: "))

        # Чтение языков, которые знает текущий школьник
        languages = {input("Введите название языка: ") for _ in range(m)}

        # Для первого школьника инициализируем оба множества
        if common_languages is None:
            common_languages = languages
        else:
            common_languages &= languages  # Пересечение для языков, известных всем школьникам

        all_languages |= languages  # Объединение всех языков

    # Разделитель
    print("\n" + "-" * 40)

    # Вывод количества и списка языков, известных всем школьникам
    print(f"Количество языков, которые знают все школьники: {len(common_languages)}")
    print("Языки, которые знают все школьники:")
    for lang in sorted(common_languages):
        print(f"  - {lang}")

    # Разделитель
    print("\n" + "-" * 40)

    # Вывод количества и списка языков, известных хотя бы одному школьнику
    print(f"Количество языков, которые знает хотя бы один школьник: {len(all_languages)}")
    print("Языки, которые знает хотя бы один школьник:")
    for lang in sorted(all_languages):
        print(f"  - {lang}")

    # Финальный разделитель
    print("-" * 40)


# Пример вызова функции
polyglots()
